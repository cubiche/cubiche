namespace Cubiche\Core\Bus\Tests\Units\Middlewares\Handler\Resolver\NameOfQuery;

use Cubiche\Core\Bus\Exception\InvalidResolverException;
use Cubiche\Core\Bus\Exception\NotFoundException;
use Cubiche\Core\Bus\Middlewares\Handler\Resolver\NameOfQuery\ChainResolver;
use Cubiche\Core\Bus\Middlewares\Handler\Resolver\NameOfQuery\FromClassNameResolver;
use Cubiche\Core\Bus\Middlewares\Handler\Resolver\NameOfQuery\FromQueryNamedResolver;
use Cubiche\Core\Bus\Tests\Fixtures\Query\NearbyVenuesQuery;
use Cubiche\Core\Bus\Tests\Fixtures\Query\PublishedPostsQuery;
use Cubiche\Core\Bus\Tests\Units\TestCase;

/**
 * ChainResolver class.
 *
 * Generated by TestGenerator on 2016-04-07 at 15:40:41.
 */
class ChainResolverTests extends TestCase
{
    /**
     * Test create.
     */
    public function testCreate()
    {
        $this
            ->given($resolver1 = new \StdClass())
            ->then()
                ->exception(function () use ($resolver1) {
                    new ChainResolver([$resolver1]);
                })
                ->isInstanceOf(InvalidResolverException::class)
        ;
    }

    /**
     * Test Resolve method.
     */
    public function testResolve()
    {
        $this
            ->given($resolver1 = new FromQueryNamedResolver())
            ->and($resolver2 = new FromClassNameResolver())
            ->and($resolver = new ChainResolver([$resolver1, $resolver2]))
            ->when($result = $resolver->resolve(new PublishedPostsQuery(new \DateTime())))
            ->then()
                ->string($result)
                    ->isEqualTo(PublishedPostsQuery::class)
            ->and()
            ->when(
                $result = $resolver->resolve(
                    new NearbyVenuesQuery($this->faker->latitude(), $this->faker->longitude())
                )
            )
            ->then()
                ->string($result)
                    ->isEqualTo('aroundVenues')
        ;

        $this
            ->given($resolver = new ChainResolver([]))
            ->then()
                ->exception(function () use ($resolver) {
                    $resolver->resolve(new NearbyVenuesQuery($this->faker->latitude(), $this->faker->longitude()));
                })
                ->isInstanceOf(NotFoundException::class)
        ;
    }
}
