namespace Cubiche\Infrastructure\Cqrs\Tests\Units\Factory\Bus;

use Cubiche\Core\Bus\Middlewares\Handler\Locator\InMemoryLocator;
use Cubiche\Core\Cqrs\Command\CommandBus;
use Cubiche\Infrastructure\Cqrs\Factory\Bus\CommandBusFactory;
use Cubiche\Infrastructure\Cqrs\Factory\HandlerClassResolverFactory;
use Cubiche\Infrastructure\Cqrs\Tests\Units\TestCase;

/**
 * CommandBusFactoryTests class.
 *
 * Generated by TestGenerator on 2017-05-03 at 11:41:18.
 */
class CommandBusFactoryTests extends TestCase
{
    /**
     * {@inheritdoc}
     */
    protected function createFactory()
    {
        return new CommandBusFactory();
    }

    /**
     * Test Create method.
     */
    public function testCreate()
    {
        $handlerClassResolverFactory = new HandlerClassResolverFactory(new InMemoryLocator([]));

        $this
            ->given($factory = $this->createFactory())
            ->and(
                $bus = $factory->create(
                    $handlerClassResolverFactory->createForCommand(),
                    $handlerClassResolverFactory->createForCommandValidator()
                )
            )
            ->then()
                ->object($bus)
                    ->isInstanceOf(CommandBus::class)
                ->and($reflectionClass = new \ReflectionClass(CommandBus::class))
                ->and($reflectionProperty = $reflectionClass->getProperty('middlewares'))
                ->and($reflectionProperty->setAccessible(true))
                ->array($reflectionProperty->getValue($bus)->toArray())
                    ->hasSize(3)
        ;
    }
}
