namespace Cubiche\Domain\EventBus\Tests\Units\Middlewares\Notifier;

use Cubiche\Domain\EventBus\Middlewares\Notifier\NotifierMiddleware;
use Cubiche\Domain\EventBus\Notifier;
use Cubiche\Domain\EventBus\Tests\Fixtures\LoginUserEvent;
use Cubiche\Domain\EventBus\Tests\Fixtures\LoginUserEventListener;
use Cubiche\Domain\EventBus\Tests\Units\TestCase;

/**
 * NotifierMiddleware class.
 *
 * Generated by TestGenerator on 2016-04-11 at 15:18:25.
 */
class NotifierMiddlewareTests extends TestCase
{
    /**
     * Test Handle method.
     */
    public function testHandle()
    {
        $this
            ->given($notifier = new Notifier())
            ->and($middleware = new NotifierMiddleware($notifier))
            ->and($event = new LoginUserEvent('ivan@cubiche.com'))
            ->and($notifier->addListener($event->name(), array(new LoginUserEventListener(), 'onLogin')))
            ->and($notifier->addListener($event->name(), function (LoginUserEvent $event) {
                $this
                    ->string($event->email())
                    ->isEqualTo('info@cubiche.org')
                ;

                $event->setEmail('fake@email.com');
            }))
            ->and($callable = function (LoginUserEvent $event) {
                $event->setEmail('callback@email.com');
            })
            ->when($result = $middleware->handle($event, $callable))
            ->then()
                ->string($event->email())
                    ->isEqualTo('callback@email.com')
        ;
    }

    /**
     * Test Notifier method.
     */
    public function testNotifier()
    {
        $this
            ->given($notifier = new Notifier())
            ->and($middleware = new NotifierMiddleware($notifier))
            ->when($result = $middleware->notifier())
            ->then()
                ->object($result)
                    ->isEqualTo($notifier)
        ;
    }
}
